# Maximum Edge of a Triangle
# Examples
# nextEdge(8, 10) ➞ 17
# nextEdge(5, 7) ➞ 11
# nextEdge(9, 2) ➞ 10

def next_edge(side1, side2):
    max_edge = side1 + side2 - 1
    return(max_edge)

# Create a function that takes two numbers as arguments and returns their sum.
# addition(3, 2) ➞ 5
# addition(-3, -6) ➞ -9
# addition(7, 3) ➞ 10

def addition(a, b):
    sum_up = a + b
    return(sum_up)
