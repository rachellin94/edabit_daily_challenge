# Return the Next Number from the Integer Passed
# Examples
# addition(0) ➞ 1
# addition(9) ➞ 10
# addition(-3) ➞ -2

def addition(num):
    new_number = num + 1 
    return new_number

# Convert Age to Days
# calc_age(65) ➞ 23725
#calc_age(0) ➞ 0
# calc_age(20) ➞ 7300

def calc_age(age):
    calc_days = age * 365
    return calc_days
